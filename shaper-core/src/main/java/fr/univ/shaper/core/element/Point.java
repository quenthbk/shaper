package fr.univ.shaper.core.element;

/**
 * Représente un point
 */
public class Point {

    /**
     * La coordonnée x
     */
    private double x;

    /**
     * La coordonnée y
     */
    private double y;

    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Point{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
